                  <h2>IBI: La Storia</h2>
                  <h3>La nostra storia, i nostri valori</h3><img src="/img/storia.jpg" class="img-responsive">
                  <p>Raccontare la storia di Ibi vuol dire parlare  di un passato ricco di soddisfazioni,  di imprenditorialità e capacità. Un passato fatto di uomini, creatività,  strategie lungimiranti, di volontà nella ricerca del miglioramento continuo e di determinazione nell’affrontare le sfide: tutto questo è Ibi, una storia ed una realtà italiane.</p>
                  <p>L’Istituto Biochimico Italiano Giovanni Lorenzini S.p.a. (IBI), una delle realtà farmaceutiche tradizionali nel panorama italiano, venne fondato a Milano nel 1918 dal Prof. Giovanni Lorenzini.  Sotto la sua guida il lavoro della Ibi venne finalizzato alla ricerca ed alla produzione farmaceutica, in particolare nel campo delle vitamine. Proprio agli albori degli studi su queste sostanze, vennero sviluppate in IBI ricerche sui metodi di estrazione, dosaggio, stabilizzazione delle vitamine allora appena scoperte e venne elaborato il primo prodotto polivitaminico per uso terapeutico, la ben nota Vitamina Lorenzini.</p>
                  <p>Grazie al desiderio del Prof. Lorenzini di voler offrire un servizio alla società è nata la missione “Fare per Dare” che la IBI persegue dunque fin dalla sua fondazione e che gli ha permesso durante il periodo delle due Guerre mondiali di svolgere un ruolo sociale importante e di soddisfare il fabbisogno nazionale di acido ascorbico, tiamina ed insulina.</p>
                  <p>A partire dal 1940 inizia la “conduzione femminile” della IBI, infatti, alla morte del suo fondatore, l’azienda passa sotto la direzione della figlia Loredana Lorenzini e rivolge tutte le sue attenzioni allo sviluppo del settore specialità etiche per il mercato italiano ed all’esportazione in tutto il mondo di prodotti finiti e in bulk. A partire dal 1956 gli antibiotici diventano il punto focale della ricerca IBI, che sviluppa metodi produttivi più efficienti di nuovi derivati semisintetici di antibiotici garantendone la qualità.</p>
                  <p>A testimonianza dell’elevato livello qualitativo raggiunto, la Ibi è una delle prime aziende italiane ad ottenere nel 1961 la prima certificazione FDA all’esportazione in USA di antibiotici beta lattamici ed orali. Negli anni ’70 si apre un periodo di cessione di Know How che la vede impegnata nella costruzione di importanti impianti di produzione di antibiotici all’estero ( Algeria, Cuba, Nigeria, Egitto, Panama). Si rende necessario un ampliamento alla sede amministrativa e dirigenziale ormai ubicata in pieno centro a Milano, nel 1974  si inaugura l’unità produttiva di Aprilia, oggi sede aziendale. In questi anni, sotto la guida di questa donna tenace e  determinata l’azienda arriva ad essere negli anni ottanta una tra le prime industrie farmaceutiche nel panorama italiano.</p>
                  <p>Nel 1990 assistiamo ad un ricambio generazionale. A Loredana Lorenzini succede  la nipote Camilla Borghese Khevenhüller, attuale presidente e amministratore delegato. Dopo circa 70 anni dalla sua nascita la IBI si trova a dover affrontare i profondi mutamenti del settore farmaceutico italiano. Inizia in questi anni una riorganizzazione che prevede il rinnovo del listino dei prodotti , il potenziamento della struttura commerciale in Italia.  La razionalizzazione delle risorse porta alla concentrazione nell’unico sito di Aprilia sia della produzione negli anni  novanta che degli uffici nel 2003. Il rinnovamento del management da un nuovo impulso allo sviluppo aziendale consolidando ed espandendo sia i mercati nazionali che internazionali. Ibi diventa così un marchio riconosciuto nel mondo sia come esportatore di materie prime sia di prodotti confezionati.</p>
                  <p>Nel 2004 IBI è il fornitore privilegiato di importanti aziende in UK, USA, Germania ed Australia. Le prospettive di  crescita nel mercato estero prospettano la necessità di produrre maggiori volumi, viene così inaugurato nel 2007 un nuovo edificio interamente dedicato alla produzione di beta lattamici.</p>
                  <p>Nel 2006 Ibi avverte la necessità di adeguare la propria organizzazione ad una dimensione europea per rispondere  in modo adeguato all’affermarsi delle aziende specializzate nei farmaci equivalenti. Insieme ad un noto gruppo europeo viene fondata la Ibisqus, dedicata alla commercializzazione in ospedale in UK, Italia e Germania.</p>
                  <p>Anche nel presente la IBI persegue la vocazione di continuare a rappresentare una moderna e creativa realtà nel panorama farmaceutico nazionale. Non si ferma davanti agli scenari in continuo movimento e neanche di fronte alle nuove tecnologie facendole proprie. In questa prospettiva di sviluppo la IBI si è affiancata al mondo della Nanomedicina che si sta sempre più affermando. Ha rinnovato il proprio Know How ed i laboratori ed è pronta per nuove sfide per offrire strumenti e prodotti in linea con la missione iniziale del suo fondatore “fare per dare” al fine di migliorare la qualità della vita.</p>
