                  <h2>IBI: 90 anni di esperienza</h2>
                  <h3>La nostra storia, i nostri valori</h3><img src="/img/home-content-img.jpg" class="img-responsive">
                  <p>L’Istituto Biochimico Italiano, Ibi, fondato dal Prof. Giovanni Lorenzini, è un’azienda farmaceutica di grande tradizione scientifica presente sul mercato italiano da più di 90 anni, con obiettivi di ricerca, innovazione e produzione. Oltre la tradizione storica aziendale, le nuove tecnologie applicate e un’attenta cura nella produzione, fanno della Ibi sinonimo di affidabilità e garanzia di alta qualità nei paesi in cui l’Azienda è presente.</p>
                  <p>Ibi dedica la propria attività allo sviluppo di nuove applicazioni terapeutiche per offrire soluzioni alternative a medici e pazienti ed è oggi una realtà consolidata in Italia, Europa, Asia, Australia e US.</p>
                  <p>La sede si trova ad Aprilia dove, oltre allo stabilimento di produzione, sono collocate tutte le funzioni aziendali. La produzione e il controllo qualità sono il core business dell’Azienda, a cui si affiancano Ricerca e Sviluppo, Farmacovigilanza, Informazione Scientifica e tutti i servizi che ruotano intorno alla vita del farmaco, in una realtà aziendale consolidata in Italia, Europa, Asia, Australia e US.</p>
                  <p>Con i suoi 18.000 mq di impianti ed un edificio completamente dedicato alla produzione di penicilline β-lattamiche, Ibi offre servizi e tecnologia all’avanguardia che la posizionano tra le prime 10 aziende italiane nella produzione conto terzi di diverse molecole. Ibi è oggi presente in più di 30 paesi con l’esportazione di gran parte dei suoi prodotti, pari ai 2/3 della produzione ed è una delle poche aziende, fuori dal territorio americano, ad aver ottenuto l’approvazione dell’FDA per la produzione di penicilline sterili per il mercato statunitense.</p>
                  <p>Ibi ha sviluppato una strategia di distribuzione negli ospedali affidando la commercializzazione dei prodotti ad una struttura dedicata, Ibisqus, oggi presente in UK, Germania ed Italia. Sul mercato italiano è presente con diversi prodotti, dalle penicilline sterili agli integratori alimentari. Ha una presenza consolidata nell’antibioticoterapia e in gastroenterologia.</p>
                  <p>Ibi raggiunge direttamente 4.000 farmacie e 3.000 specialisti grazie ad una rete di informatori scientifici qualificati e, indirettamente, oltre 6.000 medici di base con attività di direct mailing, detailing e newsletter.</p>
                  <p>Sul mercato italiano è presente con una varietà di prodotti, dalle penicilline sterili agli integratori alimentari con una presenza consolidata nell’antibioticoterapia oltre diverse aree terapeutiche, quali gastroenterologia, anestesiologia e area infiammazione e dolore.</p>
