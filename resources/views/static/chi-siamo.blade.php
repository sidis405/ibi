                  <h2>IBI: Trasferimenti di valore</h2>
                  <p>
                    Obbligo di trasparenza
                    5.1 Ogni azienda farmaceutica deve documentare e rendere pubblici ogni anno attraverso un apposito Modello che costituisce parte integrante del presente Codice, i trasferimenti di valore effettuati direttamente o indirettamente con gli Operatori sanitari e con le Organizzazioni Sanitarie, come individuati nelle Definizioni allegate (all.2). La pubblicazione dei dati dovrà avvenire su base individuale e,	l’eventuale pubblicazione in forma aggregata di cui al successivo punto 5.5, dovrà rappresentare una circostanza del tutto eccezionale. I dati dovranno essere pubblicati sul website aziendale.									 
                  </p>
                  <p>5.3 La pubblicazione dei dati connessi ai trasferimenti di valore dovrà essere effettuata su base annuale a partire dal 2016 con riferimento ai dati riguardanti il 2015. Le aziende farmaceutiche potranno indicare i trasferimenti di valore scegliendo esclusivamente il criterio di cassa o di competenza.</p>
                  <p>[…] Le aziende farmaceutiche dovranno rendere pubblici i trasferimenti di valore effettuati nel corso di ciascun anno, entro i primi sei mesi dell’anno successivo. Le informazioni dovranno rimanere di dominio pubblico per un periodo di almeno 3 anni dal momento della pubblicazione.</p>
                  <p>
                    5.5 Ogni azienda farmaceutica dovrà rendere pubblico, su base individuale per ciascun destinatario, l’ammontare dei trasferimenti di valore effettuati nel corso dell’anno precedente con riferimento a:
                    <br>
                    a) spese di partecipazione a convegni e congressi con riguardo a quota di iscrizione, viaggio e ospitalità (esclusi pasti e bevande);
                    <br>
                    b) spese per attività di consulenza e prestazioni professionali non rientranti nelle attività di cui alla precedente lettera a), risultanti	da uno specifico contratto tra l’azienda e il singolo Operatore da cui risulti la tipologia del servizio prestato.								 
                  </p>
                  <p>
                    […]Qualora l’Operatore non presti il proprio consenso al trattamento dei dati personali, le aziende dovranno comunque provvedere alla pubblicazione dei dati su base aggregata. In tale ipotesi dovrà essere identificabile per ciascuna delle categorie individuate alle precedenti lettere a) e b):
                    <br>
                    - il numero dei destinatari su base assoluta e come percentuale sul totale dei destinatari;
                    <br>
                    - il dato aggregato attribuibile a quegli Operatori sanitari;
                    <br>
                    - il dato percentuale dei trasferimenti di valore in forma aggregata sul totale dei trasferimenti.									 
                  </p>
                  <p>
                    5.6 Ogni azienda dovrà rendere pubblico l’ammontare dei trasferimenti di valore effettuati nei confronti di ciascuna Organizzazione sanitaria, come individuata nelle definizioni allegate al presente Codice, nel corso dell’anno precedente con riferimento a:
                    <br>
                    a) donazioni e contributi (inclusi i comodati d’uso) sia in denaro che in natura;
                    <br>
                    b) finanziamenti diretti o indiretti ad eventi congressuali, effettuati tramite strutture sanitarie o terze parti, inclusa la sponsorizzazione dei medici a convegni e congressi con il pagamento della quota di iscrizione o delle spese di viaggio e ospitalità;
                    <br>
                    c) transazioni economiche relative a consulenze e prestazioni professionali risultanti da un contratto scritto tra aziende farmaceutiche e Istituzioni, Organizzazioni o Associazioni che forniscono qualsiasi tipo di servizio non ricompreso nelle precedenti categorie a) e b).									 
                  </p>
                  <p>
                    5.8 Le spese sostenute annualmente dalle aziende farmaceutiche per attività di ricerca e sviluppo dovranno essere rese pubbliche in forma aggregata. Rientrano tra queste attività quelle finalizzate alla pianificazione o alla realizzazione di:
                    <br>
                    a) studi non clinici, come definiti dalle Good Laboratory Practice;
                    <br>
                    b) studi clinici, come definiti dalla Direttiva 2001/20/CE;
                    <br>
                    c) studi osservazionali prospettici, di cui al punto 4.4 del presente Codice, che coinvolgano la raccolta di dati sui pazienti da parte dei singoli medici o di gruppi di medici.
                  </p>
                  <p>5.9 Dovranno essere rese pubbliche su base aggregata anche le spese relative ad Investigator Meetings, Adivisory Board o ospitalità laddove tali spese siano connesse alle attività di cui al precedente punto 5.8.</p>
