                          <h4 class="panel-title">
                            												
                            Steatosi, steatoepatite alcolica e non alcolica
                          </h4>
                          <div class="plus-minus minus">							</div>
                        </div></a>
                      <div id="collapseOne" role="tabpanel" aria-labelledby="headingOne" class="panel-collapse collapse in">
                        <div class="panel-body">																			
                          <p>
                            La steatosi epatica non alcolica o NAFLD (Non Alcoholic Fatty Liver Disease) è la più comune causa di malattia cronica del fegato nei paesi occidentali. La malattia è caratterizzata dall’accumulo di grasso nel fegato (NAFL), in particolare di trigliceridi all’interno delle cellule (epatociti), e può evolvere in steatoepatite o NASH (Non Alcoholic Steato-Hepatitis) e si caratterizza per la presenza, oltre ai depositi di grasso, per una reazione necro-infiammatoria.
                            Quando invece la steatoepatite è correlata principalmente ad una assunzione elevata di alcol viene definita ASH (Alcoholic Steato-Hepatitis).											
                          </p>
                        </div>
                      </div>
                    </div>
                    <div class="panel panel-default"><a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo" class="collapsed">
                        <div id="headingTwo" role="tab" class="panel-heading">
                          <h4 class="panel-title">
                            												
                            Epidemiologia, cause e storia naturale
                          </h4>
                          <div class="plus-minus"></div>
                        </div></a>
                      <div id="collapseTwo" role="tabpanel" aria-labelledby="headingTwo" class="panel-collapse collapse">
                        <div class="panel-body">
                          <p>
                            La Steatosi epatica non alcolica è presente nel 10-25% della popolazione generale, quindi in media 1 italiano su 4 ne soffre; tali percentuali aumentano in modo significativo nei pazienti obesi, fino a valori del 50-90%.
                            L’entità di questo problema non è da sottovalutare, non solo perché la percentuale di obesi nel nostro paese è significativa (18%) ma soprattutto perché si osserva da anni un trend di progressivo aumento (vedi confronto tra anni ’90 e primi anni del 2000 con un aumento degli obesi dal 10 al 18%, ovviamente associato anche ad un aumento del numero delle persone in sovrappeso).
                          </p><img src="http://placehold.it/750x350" alt="" class="img-responsive">
                          <p>
                            La steatosi, se si attuano le opportune misure eliminando le cause che l’hanno prodotta, è un danno stabile o reversibile. Purtroppo, essendo un danno iniziale, non facilmente diagnosticabile per la mancanza di segni e sintomi clinici evidenti, può evolvere in steatoepatite. La Steatoepatite è presente nel 5-10% dei pazienti con NAFLD e si instaura per la costante presenza delle cause/fattori di rischio che hanno condotto alla NAFLD. Tali cause/fattori di rischio sono legate principalmente ad una non corretta alimentazione e allo stile di vita, fattori che inevitabilmente portano ad obesità, ipercolesterolemia, ipertrigliceridemia, insulino-resistenza e diabete tipo 2. Oltre queste cause/fattori di rischio più comuni la NAFLD si riscontra in seguito a farmaci assunti cronicamente o tossici per il fegato, diete ipocaloriche drastiche non bilanciate, disordini metabolici ereditari, danno intestinale. Non ultima causa di NAFLD è l’epatite da virus C.
                            La steatoepatite progredisce più o meno lentamente verso la fibrosi e la cirrosi. Inoltre, è spesso associata a un aumentato rischio di malattia cardiovascolare e diabete mellito di tipo 2. 
                            Negli ultimi anni si sta valutando attentamente quanti pazienti affetti dall’epatopatia cronica possano andare incontro all' epatocarcinoma, senza necessariamente avere una cirrosi, legata ai virus dell’epatite virale C.												
                          </p>
                        </div>
                      </div>
                    </div>
