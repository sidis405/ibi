                  <h4>Il problema</h4>
                  <p>
                    Il meteorismo è un disturbo frequente, dovuto alla presenza di quantità eccessive di gas nello stomaco e nell’intestino.
                    Si manifesta con una sintomatologia caratterizzata inizialmente con fastidio addominale, senso di gonfiore e rumori intestinali (borborigmi), che, protraendosi nel tempo, possono complicarsi in forti dolori addominali e crampi.
                    Il meteorismo si innesca durante il processo digestivo, quando gli zuccheri e la cellulosa “fermentano” nell’intestino, con la conseguente formazione di azoto, metano e anidride carbonica.
                    Una delle cause più diffuse di questo disturbo è il cattivo funzionamento del colon, conosciuto come sindrome del colon irritabile, ma può derivare anche da intolleranze alimentari o, più semplicemente, dal consumo di alcune fibre non idrosolubili, che tendono a fermentare e, di conseguenza, ad aumentare la produzione di gas.
                    Gas di cui sono ricchi alcuni vegetali come i legumi (fagioli, piselli secchi e lenticchie), ma anche altre verdure a foglia verde (cavoli, broccoli e cavolfiori), la soia, i ravanelli, le cipolle, i cetrioli e i peperoni.
                    Anche i cibi grassi (fritti, salse), quelli con alto contenuto di lattosio (latte, gelato, creme) e i dolcificanti artificiali (sorbitolo, mannitolo e prodotti che li contengono) possono favorire l’accumulo di gas nello stomaco e nell’intestino, come anche l’abuso di tè, caffè o bevande gassate.
                    Oltre alla fermentazione di zuccheri e carboidrati dovuta a cattiva digestione, il meteorismo può avere un’altra causa: l’aerofagia, cioè l’ingestione di aria durante la deglutizione di cibi e bevande, che provoca nello stomaco il tipico gonfiore.
                    Questo disturbo, dovuto alla formazione di bollicine d’aria nella saliva, che vengono di seguito ingerite, dipende spesso da cattive abitudini come mangiare velocemente, masticare a lungo un chewing-gum o fumare.
                  </p>
                  <h4>La soluzione</h4>
                  <p>
                    Oltre a modificare il proprio stile di vita e abitudini alimentari, Meteosim può rappresentare una valida soluzione al meteorismo e all’aerofagia. È un medicinale di automedicazione che favorisce l’eliminazione dei gas nel tratto gastrointestinale.
                    L’efficacia di Meteosim è dovuta all’azione del simeticone (dimeticone attivato), una miscela di polimeri di silicone combinati con diossido di silicio, che agisce localmente nell’apparato gastrointestinale ed è capace di abbassare la tensione superficiale “sequestrando” il gas in eccesso e favorendone l’eliminazione.
                    Il simeticone fa in modo che le bolle di gas presenti confluiscano per poi essere eliminate più facilmente alleviando i fastidiosi sintomi associati al meteorismo e all’aerofagia.
                    Il simeticone non viene assorbito dall’organismo e non interferisce sull’assorbimento delle sostanze nutritive agendo pertanto solo sui gas.
                    L’ampio campione di pazienti trattati in oltre 40 anni di utilizzo e l’elevato numero di studi disponibili testimoniano l’efficacia di simeticone nel ridurre la sintomatologia da eccessiva presenza di aria nel tratto gastrointestinale, indicazione confermata dalla Food and Drug Administration già nel 1973.
                    Meteosim - simeticone - si può usare per curare disturbi lievi e transitori, facilmente riconoscibili e risolvibili: può essere quindi acquistato senza ricetta ma va usato correttamente per assicurarne l’efficacia. Il medicinale non presenta particolari controindicazioni, salvo per ipersensibilità al principio attivo o ad altri componenti del prodotto. È generalmente controindicato nelle donne in gravidanza.
