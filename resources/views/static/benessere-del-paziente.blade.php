                            Non esiste ad oggi una terapia standardizzata della NAFLD.
                            Modificare il proprio stile di vita è fondamentale per una significativa riduzione della steatosi e una normalizzazione degli esami clinici. L’utilizzo di integratori antiossidanti è utile per prevenire l’evoluzione del danno, recuperare la funzionalità epatica, riequilibrare il metabolismo.
                            RealSIL, un integratore, a base di silibina con fosfolipidi e vitamina E, protegge gli epatociti dal danno infiammatorio dei radicali liberi, come stabilizzatore di membrana e antiossidante naturale, ed è in grado di “depurare il fegato grasso” e rallentarne il danno, riequilibrando l’assetto metabolico, alla base dell’accumulo di grassi nel fegato. Dagli studi fatti con RealSIL si è visto che il prodotto normalizza gli indici di funzionalità epatica (ALT e YGT), riduce il grado di steatosi epatica, contrasta l’infiammazione epatica e sistemica, riduce l’iperglicemia e migliora la sensibilità all’insulina.
                            I risultati uno studio randomizzato in doppio cieco multicentrico finalizzato a dimostrare l’attività di questo integratore sono molto incoraggianti e indicano come le sostanze che lo compongono possano essere in grado di agire sulla malattia. Va inoltre sottolineata l’ottima tollerabilità e compliance per i pazienti, anche per dosaggi elevati e per lunghi periodi.
