                        <div class="panel-body">
                          <p>
                            Da sempre si è cercato di ottenere informazioni sullo stato funzionale e sull' architettura del fegato utilizzando le informazioni derivate da indagini non invasive (ad es. l’ecografia e gli esami del sangue), tuttavia al momento nessun test ematico da solo è in grado di dare una risposta completa. Negli ultimi anni si è cercato di ovviare a questo problema studiando la possibile combinazione di dati generali del paziente al fine di ottenere una risposta “indiretta”.
                            Di recente, nella pratica clinica sono stati introdotti alcuni biomarcatori che potrebbero aiutare a selezionare i pazienti con epatopatia iniziale o cronica.
                            FIBROTEST
                            Rappresenta la combinazione di marcatori indiretti più studiata finora, prevede la combinazione di esami del sangue quali GGT, bilirubina totale, aptoglobina, α1 e α2 macroglobulina.
                            Fibrotest può essere utilizzato per pazienti con epatite virale, alcolica o metabolica per la valutazione del danno epatico. Permette di classificare i pazienti per stadio oltre a determinare l’attività necroinfiammatoria virale.
                            FIBROMAX
                            Il FibroMax fornisce informazioni sul grado di steatosi, flogosi e fibrosi. Questo test utilizza un sistema basato su alcuni parametri del paziente come sesso, età, peso, altezza e alcuni esami del sangue (α2macroglobulina, aptoglobulina, apolipoproteina A1, bilirubina totale, AST, ALT, GGT, glicemia a digiuno, trigliceridi e colesterolo totale).
                            Come per il Fibrotest può essere utilizzato per pazienti con epatite virale, alcolica o metabolica per la valutazione del danno epatico.
                            Negli studi condotti finora, per valutare l’efficacia di questi test, si evidenziano dei dati incoraggianti, utili per lo screening della fibrosi avanzata o della NASH in pazienti con sindrome metabolica.
                            FIBROSCAN
                            E’ una tecnica che è stata sviluppata di recente per la valutazione della fibrosi epatica, viene anche chiamata elastografia transitoria. Permette di valutare la rigidità del fegato con uno strumento che è simile ad un ecografo, secondo il presupposto che il tessuto del fegato, quando va in contro alla fibrosi diventa più “duro”.
                            L’esame si esegue mediante una sonda che, appoggiata tra gli spazi intercostali al di sopra del fegato, invia un piccolo impulso ad onda meccanica alla cute e ne registra il tempo di “ritorno”: con tale metodica lo strumento riesce a registrare la velocità di propagazione delle onde e stabilire quindi la rigidità del fegato.
                          </p><img src="http://placehold.it/750x350" alt="" class="img-responsive">
                        </div>
