                          <h4 class="panel-title">
                            												
                            	Sintomatologia e segni clinici
                          </h4>
                          <div class="plus-minus">								</div>
                        </div></a>
                      <div id="collapseThree" role="tabpanel" aria-labelledby="headingThree" class="panel-collapse collapse">
                        <div class="panel-body">
                          <p>
                            La maggior parte dei pazienti con NAFLD è asintomatica, o presenta una sintomatologia sfumata.
                            Spesso la diagnosi è occasionale dopo un controllo generale (esami del sangue, ecografia). Alcuni pazienti riferiscono un senso di malessere, stanchezza o una vaga sensazione di dolenzia a livello del quadrante addominale destro (vedi la sede del dolore nell’immagine), difficoltà digestiva. Si può riscontrare epatomegalia e sovrappeso.
                          </p><img src="http://placehold.it/750x350" alt="" class="img-responsive">
                        </div>
                      </div>
